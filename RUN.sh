#!/bin/bash

echo "=================================================="
echo "Запуск Информационной Системы 'ИС КАНО'"
echo "=================================================="

# Проверяем, существует ли виртуальное окружение
if [ ! -f ".venv/bin/activate" ]; then
    echo "[ОШИБКА] Виртуальное окружение .venv не найдено!"
    echo "Убедитесь, что вы создали его и установили зависимости:"
    echo "python3 -m venv .venv"
    echo "source .venv/bin/activate"
    echo "pip install -r requirements.txt"
    exit 1
fi

echo "[1/3] Активация виртуального окружения..."
source .venv/bin/activate

echo "[2/3] Запуск Flask сервера (app.py)..."
echo "[3/3] Сервер будет доступен по адресу: http://127.0.0.1:8000/"
echo "=================================================="

python3 app.py